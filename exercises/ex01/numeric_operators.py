left_hand_side=int(input("Left-hand side: "))
right_hand_side=int(input("Right-hand side: "))
print(str(left_hand_side)+str(" ** ")+ str(right_hand_side)+" is "+str(left_hand_side ** right_hand_side))
print(str(left_hand_side)+str(" / ")+ str(right_hand_side)+" is "+str(left_hand_side / right_hand_side))
print(str(left_hand_side)+str(" // ")+ str(right_hand_side)+" is "+str(left_hand_side // right_hand_side))
print(str(left_hand_side)+str(" % ")+ str(right_hand_side)+" is "+str(left_hand_side % right_hand_side))